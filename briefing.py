# ── 4. Synthesize Narratives with Claude (no table) ───────────────────────────

def synthesize_narratives(macro: dict, macro_news: str, prices: dict, news: dict) -> str:
    """Claude writes ONLY the narrative sections. Table is built in Python."""
    client = Anthropic(api_key=ANTHROPIC_KEY)
    today  = datetime.date.today().strftime("%A %d %B %Y")

    system_prompt = (
        "You are a senior equity analyst writing a pre-market briefing for a portfolio manager. "
        "Write in institutional register: dense, declarative, no marketing language. "
        "Display prices in their native currency (field 'currency' in data). "
        "Output ONLY the narrative sections below — do NOT write a summary table, I will add it separately.\n\n"
        "SECTIONS TO WRITE (use these exact HTML tags):\n"
        "<h3>MACRO OVERNIGHT</h3>\n"
        "3-4 sentences synthesising futures, oil, rates, key headlines. "
        "Connect macro to watchlist explicitly: Brent/WTI → ECO/TRMD/FRO/TEN; "
        "EUR/USD → DOV/INTRUM; 10Y yield → ALPHA. Flag moves >1% with ⚠️.\n\n"
        "<h3>ALERTS</h3>\n"
        "Flag stock moves >2% or material news. Omit section entirely if none.\n\n"
        "<h3>PRIVATE EQUITY</h3>\n"
        "TPG: one paragraph (price → volume → news → implication).\n\n"
        "<h3>GREEK BANKS</h3>\n"
        "Alpha Bank: one paragraph.\n\n"
        "<h3>TANKER SHIPPING</h3>\n"
        "ECO, TRMD, FRO, TEN: one paragraph each.\n\n"
        "<h3>CREDIT / NPL SERVICERS</h3>\n"
        "doValue, Intrum: one paragraph each.\n\n"
        "Be concise. 3-4 sentences per paragraph maximum. No table. Clean HTML only."
    )

    user_prompt = f"""Date: {today}

MACRO DATA: {json.dumps(macro)}

MACRO NEWS:
{macro_news}

STOCK DATA: {json.dumps(prices)}

STOCK NEWS:
{"".join([f"[{t}] {n}\n" for t, n in news.items()])}"""

    response = client.messages.create(
        model      = "claude-sonnet-4-6",
        max_tokens = 3000,
        system     = system_prompt,
        messages   = [{"role": "user", "content": user_prompt}],
    )
    return response.content[0].text.replace("```html", "").replace("```", "").strip()


# ── 5. Build Summary Table in Python ──────────────────────────────────────────

def build_summary_table(prices: dict) -> str:
    """Builds the summary table directly from price data — never truncated."""
    def arrow(chg):
        if chg is None: return "—"
        return f"{'▲' if chg >= 0 else '▼'}{abs(chg)}%"

    def color(chg):
        if chg is None: return "#888"
        return "#1a7a3f" if chg >= 0 else "#c0392b"

    rows = ""
    for ticker, d in prices.items():
        if "error" in d:
            rows += f"""<tr>
              <td><b>{ticker}</b></td>
              <td>{d.get('name','—')}</td>
              <td colspan="6" style="color:#888">Data unavailable</td>
            </tr>"""
            continue

        cur   = d.get("currency", "")
        close = f"{cur} {d['close']}" if d.get("close") else "—"
        chg   = d.get("pct_change")
        vr    = f"{d['vol_ratio']}x" if d.get("vol_ratio") else "—"
        h52   = f"{cur} {d['week52_high']}" if d.get("week52_high") else "—"
        pfh   = f"{d['pct_from_high']}%" if d.get("pct_from_high") is not None else "—"
        chg_s = arrow(chg)
        chg_c = color(chg)

        rows += f"""<tr>
          <td><b>{ticker}</b></td>
          <td style="font-size:11px">{d.get('name','—')}</td>
          <td>{close}</td>
          <td style="color:{chg_c};font-weight:600">{chg_s}</td>
          <td>{vr}</td>
          <td>{h52}</td>
          <td>{pfh}</td>
        </tr>"""

    return f"""
<table width="100%" cellpadding="0" cellspacing="0"
       style="border-collapse:collapse;font-size:12px;margin-top:8px">
  <tr style="background:#003d4d;color:#fff">
    <th style="padding:7px 9px;text-align:left">Ticker</th>
    <th style="padding:7px 9px;text-align:left">Name</th>
    <th style="padding:7px 9px;text-align:left">Last</th>
    <th style="padding:7px 9px;text-align:left">Chg%</th>
    <th style="padding:7px 9px;text-align:left">Vol/Avg</th>
    <th style="padding:7px 9px;text-align:left">52W High</th>
    <th style="padding:7px 9px;text-align:left">% from High</th>
  </tr>
  {rows}
</table>"""


# ── 6. Build Macro Bar ────────────────────────────────────────────────────────

def build_macro_bar(macro: dict) -> str:
    def cell(label, key):
        d = macro.get(key, {})
        if "error" in d or not d:
            val = "N/A"
            chg_html = ""
        else:
            val  = d["last"]
            chg  = d["chg_pct"]
            col  = "#7ee8a2" if chg >= 0 else "#ff9999"
            arr  = "▲" if chg >= 0 else "▼"
            chg_html = f" <span style='color:{col}'>{arr}{abs(chg)}%</span>"
        return (f"<td style='padding:6px 12px;color:#d0e8ee;font-size:11px;"
                f"white-space:nowrap;border-right:1px solid #024055'>"
                f"{label}<br><b style='font-size:12.5px;color:#fff'>{val}</b>{chg_html}</td>")

    cells = (
        cell("S&amp;P Fut",  "S&P 500 Futures") +
        cell("NQ Fut",       "Nasdaq 100 Futures") +
        cell("Brent",        "Brent Crude (USD/bbl)") +
        cell("WTI",          "WTI Crude (USD/bbl)") +
        cell("US 10Y",       "US 10Y Yield (%)") +
        cell("EUR/USD",      "EUR/USD") +
        cell("SEK/USD",      "SEK/USD")
    )
    return (f"<table width='100%' cellpadding='0' cellspacing='0' "
            f"style='background:#012e3a'><tr>{cells}</tr></table>")


