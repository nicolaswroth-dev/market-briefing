# ── 1. Fetch Macro Data ────────────────────────────────────────────────────────

def fetch_macro() -> dict:
    macro = {}
    instruments = [
        ("ES=F",     "S&P 500 Futures"),
        ("NQ=F",     "Nasdaq 100 Futures"),
        ("BZ=F",     "Brent Crude (USD/bbl)"),
        ("CL=F",     "WTI Crude (USD/bbl)"),
        ("^TNX",     "US 10Y Yield (%)"),
        ("EURUSD=X", "EUR/USD"),
        ("SEKUSD=X", "SEK/USD"),
    ]
    for ticker, label in instruments:
        try:
            hist = yf.Ticker(ticker).history(period="5d")
            hist = hist[hist["Close"].notna()]
            if len(hist) < 2:
                macro[label] = {"error": "no data"}
                continue
            last = round(float(hist["Close"].iloc[-1]), 4)
            prev = round(float(hist["Close"].iloc[-2]), 4)
            chg  = round((last - prev) / prev * 100, 2)
            macro[label] = {"last": last, "chg_pct": chg}
        except Exception as e:
            macro[label] = {"error": str(e)}
    return macro


def fetch_macro_news() -> str:
    url     = "https://api.perplexity.ai/chat/completions"
    headers = {"Authorization": f"Bearer {PERPLEXITY_KEY}", "Content-Type": "application/json"}
    prompt  = (
        "List the 5 most important macro/market headlines from the last 12 hours for equity investors. "
        "Focus on: Fed/ECB statements, geopolitical events, commodity moves, major data releases, systemic events. "
        "Format: bullet points, one sentence each, source and time if available."
    )
    payload = {"model": "sonar", "messages": [{"role": "user", "content": prompt}]}
    try:
        r = requests.post(url, headers=headers, json=payload, timeout=30)
        r.raise_for_status()
        return r.json()["choices"][0]["message"]["content"]
    except Exception as e:
        return f"Macro news unavailable: {e}"


# ── 2. Fetch Stock Prices ──────────────────────────────────────────────────────

def fetch_prices(tickers: list) -> dict:
    data = {}
    for ticker in tickers:
        try:
            stock = yf.Ticker(ticker)
            hist = None
            for period in ["5d", "1mo"]:
                h = stock.history(period=period)
                h = h[h["Close"].notna()]
                if len(h) >= 2:
                    hist = h
                    break
            if hist is None or len(hist) < 2:
                data[ticker] = {"error": "no data"}
                continue

            close_today = round(float(hist["Close"].iloc[-1]), 2)
            close_prev  = round(float(hist["Close"].iloc[-2]), 2)
            pct_change  = round((close_today - close_prev) / close_prev * 100, 2)
            vol         = float(hist["Volume"].iloc[-1])
            avg_vol     = float(hist["Volume"].mean())
            volume      = int(vol)     if not math.isnan(vol)     else 0
            avg_volume  = int(avg_vol) if not math.isnan(avg_vol) else 0
            vol_ratio   = round(volume / avg_volume, 2) if avg_volume > 0 else 0

            info = stock.fast_info
            try:
                week52_high   = round(float(info.year_high), 2)
                week52_low    = round(float(info.year_low),  2)
                pct_from_high = round((close_today - week52_high) / week52_high * 100, 1)
            except Exception:
                week52_high = week52_low = pct_from_high = None

            try:
                currency = info.currency
            except Exception:
                currency = "USD"

            data[ticker] = {
                "name":           WATCHLIST[ticker]["name"],
                "close":          close_today,
                "prev_close":     close_prev,
                "currency":       currency,
                "pct_change":     pct_change,
                "volume":         volume,
                "avg_volume":     avg_volume,
                "vol_ratio":      vol_ratio,
                "week52_high":    week52_high,
                "week52_low":     week52_low,
                "pct_from_high":  pct_from_high,
            }
        except Exception as e:
            data[ticker] = {"error": str(e), "name": WATCHLIST[ticker]["name"]}
    return data


# ── 3. Fetch Stock News ────────────────────────────────────────────────────────

